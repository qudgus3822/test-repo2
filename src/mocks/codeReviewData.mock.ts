// 랜덤 리뷰어 목록 생성 함수
function generateRandomReviewerList(count: number): ReviewerInfo[] {
  if (count === 0) return [];
  const shuffled = [...authorsData].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count).map((a) => ({ name: a.name, team: a.team }));
}

// 랜덤 데이터 생성 함수
export function generateRandomReviewItem(index: number): ReviewItem {
  const randomAuthor =
    authorsData[Math.floor(Math.random() * authorsData.length)];
  const randomStatus: ReviewStatus =
    Math.random() > 0.3 ? "COMPLETED" : "NOT_COMPLETED";

  const mrApprovalCount = Math.floor(Math.random() * 3) + 1;
  const reviewRequestCount = Math.floor(Math.random() * 3);
  const reviewApprovalCount = Math.floor(Math.random() * 3) + 1;

  return {
    date: "2025-11-10",
    mrId: `I${12340 + index}`,
    author: randomAuthor.name,
    authorEmail: randomAuthor.email,
    mrApproval: mrApprovalCount,
    mrApprovalList: generateRandomReviewerList(mrApprovalCount),
    reviewRequest: reviewRequestCount,
    reviewRequestList: generateRandomReviewerList(reviewRequestCount),
    reviewApproval: reviewApprovalCount,
    reviewApprovalList: generateRandomReviewerList(reviewApprovalCount),
    mrReopen: Math.floor(Math.random() * 3) + 1,
    status: randomStatus,
  };
}

// 대량 목업 데이터 생성
export function generateMockReviewData(
  totalItems: number = 156,
): CodeReviewData {
  const items: ReviewItem[] = [];

  for (let i = 0; i < totalItems; i++) {
    items.push(generateRandomReviewItem(i));
  }

  const completedCount = items.filter(
    (item) => item.status === "COMPLETED",
  ).length;
  const notCompletedCount = items.filter(
    (item) => item.status === "NOT_COMPLETED",
  ).length;

  return {
    totalMR: totalItems,
    inProgressCount: notCompletedCount,
    completedCount: completedCount,
    inProgressPercentage: Number(
      ((notCompletedCount / totalItems) * 100).toFixed(1),
    ),
    completedPercentage: Number(
      ((completedCount / totalItems) * 100).toFixed(1),
    ),
    currentPage: 1,
    totalPages: Math.ceil(totalItems / 15),
    itemsPerPage: 15,
    totalItems,
    items,
  };
}
